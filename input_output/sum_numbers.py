# This example performs concatenation number1 and number2 hold string values. 

number1 = input('Enter the first number: ')
number2 = input('Enter the second number: ')
sum = number1 + number2

print(f'The numbers {number1} and {number2} '
      f'add to {sum}')




# This example converts the number1 and number2 strings to floats, so they can be properly added.

number1 = input('Enter the first number: ')
number2 = input('Enter the second number: ')
sum = float(number1) + float(number2)

print(f'The numbers {number1} and {number2} add to {sum}')
