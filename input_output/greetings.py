# Terminal Output
# You've already seen one way to display output in the terminal: the print function. This built-in 
# function takes any Python value, regardless of type, and prints it. Let's see an example. Create 
# a file named greetings.py with the following content:

name = 'Jane'
print(f'Good morning, {name}!')

# In this simple example, we initialize a name variable with the string 'Jane', then use it in an 
# f-string interpolation to build and display a greeting message. Run the program to see it in 
# action:

# Good morning, Jane!
