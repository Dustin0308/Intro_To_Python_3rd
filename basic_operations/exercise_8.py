# To what value does the following expression evaluate?

'12' < '9'


# My Answer:

# It will evaluate to True. Python goes element by element from left to right. So it first compares
# 1 to 9. Since 1 is less than 9, Python stops there and returns True.


# Launch School's Answer: 

# The expression evaluates as True since the operands are strings, not numbers. Python performs a 
# character-by-character comparison from left to right when comparing string values. Thus, on the 
# first comparison, it determines that '1' < '9', so '12' must be less than '9'.

# If you used numbers instead of strings, the expression would be False. 
