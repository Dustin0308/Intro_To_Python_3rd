# What does the following code do? Why?

print('5' + '10')



# My Answer:

# It concatenates the strings '5' and '10' and prints the new string 510. The reason for this is 
# Python doesn't recognize the '5' and '10' as numbers, but recognizes them as strings and combines 
# the strings. 


